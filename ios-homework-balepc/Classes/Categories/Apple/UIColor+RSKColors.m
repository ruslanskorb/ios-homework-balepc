#import "UIColor+RSKColors.h"

@implementation UIColor (RSKColors)

+ (UIColor *)rsk_lightGrayColor
{
    return [UIColor colorWithHex:0xe5e5e5];
}

+ (UIColor *)rsk_grayColor
{
    return [UIColor colorWithRed:170/255.0 green:171/255.0 blue:179/255.0 alpha:1.0];
}

@end
